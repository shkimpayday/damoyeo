package com.damoyeo.api.domain.chat.entity;

/**
 * ============================================================================
 * 채팅 메시지 타입
 * ============================================================================
 *
 * [메시지 분류]
 * - TEXT: 일반 텍스트 메시지
 * - IMAGE: 이미지 메시지 (Phase 3 확장 기능)
 * - SYSTEM: 시스템 메시지 (예: "홍길동님이 입장했습니다")
 *
 * @author damoyeo
 * @since 2025-02-25
 */
public enum MessageType {
    /**
     * 일반 텍스트 메시지
     */
    TEXT,

    /**
     * 이미지 메시지
     * Phase 3에서 이미지 공유 기능 추가 시 사용
     */
    IMAGE,

    /**
     * 시스템 메시지
     * 멤버 입장/퇴장, 역할 변경 등 시스템 이벤트 알림
     */
    SYSTEM
}
