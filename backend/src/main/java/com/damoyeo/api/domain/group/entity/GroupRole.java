package com.damoyeo.api.domain.group.entity;

/**
 * ============================================================================
 * 모임 내 역할 Enum
 * ============================================================================
 *
 * [역할]
 * 회원이 특정 모임 내에서 가지는 역할을 정의합니다.
 *
 * [MemberRole과의 차이점]
 * - MemberRole: 시스템 전체에서의 권한 (USER, ADMIN)
 *   → Spring Security 인증에 사용
 * - GroupRole: 특정 모임 내에서의 역할 (OWNER, MANAGER, MEMBER)
 *   → 모임 내 권한 검사에 사용
 *
 * [예시]
 * 홍길동은:
 * - 시스템에서는 USER (MemberRole)
 * - "러닝 크루" 모임에서는 OWNER (GroupRole)
 * - "독서 모임"에서는 MEMBER (GroupRole)
 *
 * [사용 위치]
 * - GroupMember 엔티티의 role 필드
 * - GroupService에서 권한 검사 시
 *
 * [DB 저장]
 * @Enumerated(EnumType.STRING)으로 문자열 저장
 */
public enum GroupRole {

    /**
     * 모임장 (소유자)
     *
     * 모임을 생성한 사람이며, 모든 권한을 가집니다.
     *
     * [권한]
     * - 모임 정보 수정/삭제
     * - 모든 멤버 관리 (강퇴, 역할 변경)
     * - 운영진 지정/해제
     * - 정모 생성/수정/삭제
     *
     * ※ 모임당 OWNER는 1명만 존재합니다.
     * ※ Group 엔티티의 owner 필드와 일치해야 합니다.
     */
    OWNER,

    /**
     * 운영진
     *
     * 모임장이 지정한 운영 보조 역할입니다.
     *
     * [권한]
     * - 멤버 강퇴
     * - 정모 생성/수정
     * - 일부 모임 정보 수정 (Phase 2에서 세분화 예정)
     *
     * [권한 없음]
     * - 모임 삭제
     * - 다른 운영진 지정
     * - 모임장 권한 이양
     */
    MANAGER,

    /**
     * 일반 멤버
     *
     * 모임에 가입한 일반 회원입니다.
     *
     * [권한]
     * - 정모 참석/불참 등록
     * - 채팅 참여
     * - 모임 정보 조회
     *
     * [권한 없음]
     * - 모임 정보 수정
     * - 멤버 관리
     *
     * 가입 시 기본 역할입니다.
     */
    MEMBER
}
