package com.damoyeo.api.domain.group.entity;

/**
 * ============================================================================
 * 모임 상태 Enum
 * ============================================================================
 *
 * [역할]
 * 모임의 현재 상태를 나타냅니다.
 *
 * [사용 위치]
 * - Group 엔티티의 status 필드
 * - GroupService에서 모임 상태 변경 시
 * - 모임 목록 조회 시 필터링 조건
 *
 * [DB 저장]
 * @Enumerated(EnumType.STRING)으로 설정되어 문자열로 저장됩니다.
 * 예: "ACTIVE", "INACTIVE", "DELETED"
 */
public enum GroupStatus {

    /**
     * 활성 상태
     *
     * 정상적으로 운영 중인 모임입니다.
     * - 검색/목록에 노출됨
     * - 가입 신청 가능
     * - 정모 생성 가능
     *
     * 모임 생성 시 기본값입니다.
     */
    ACTIVE,

    /**
     * 비활성 상태
     *
     * 일시적으로 운영을 중단한 모임입니다.
     * - 검색/목록에서 제외될 수 있음
     * - 새로운 가입 신청 불가
     * - 기존 멤버는 유지됨
     *
     * 사용 예: 모임장의 개인 사정, 시즌 종료 등
     */
    INACTIVE,

    /**
     * 삭제된 상태 (소프트 삭제)
     *
     * 논리적으로 삭제된 모임입니다.
     * - 검색/목록에서 완전히 제외
     * - 모든 기능 비활성화
     * - DB에서 물리적으로 삭제하지 않음 (기록 보존)
     *
     * ※ 소프트 삭제(Soft Delete)를 사용하는 이유:
     * 1. 실수로 삭제한 경우 복구 가능
     * 2. 관련 데이터(정모, 채팅 등)의 참조 무결성 유지
     * 3. 통계/분석 목적의 데이터 보존
     */
    DELETED
}
