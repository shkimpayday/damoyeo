package com.damoyeo.api.domain.group.entity;

/**
 * ============================================================================
 * 모임 가입 상태 Enum
 * ============================================================================
 *
 * [역할]
 * 회원의 모임 가입 신청 상태를 나타냅니다.
 *
 * [가입 흐름]
 *
 *     사용자가 가입 신청
 *            │
 *            ▼
 *       ┌─────────┐
 *       │ PENDING │  ← 대기 상태 (초기값)
 *       └────┬────┘
 *            │
 *     ┌──────┴──────┐
 *     │             │
 *     ▼             ▼
 * ┌────────┐   ┌──────────┐
 * │APPROVED│   │ REJECTED │
 * └────────┘   └──────────┘
 *    승인          거절
 *
 * [사용 위치]
 * - GroupMember 엔티티의 status 필드
 * - GroupService.join(): PENDING으로 생성
 * - GroupService.approve(): APPROVED로 변경
 * - GroupService.reject(): REJECTED로 변경
 *
 * [DB 저장]
 * @Enumerated(EnumType.STRING)으로 문자열 저장
 */
public enum JoinStatus {

    /**
     * 대기 중
     *
     * 가입 신청 후 모임장/운영진의 승인을 기다리는 상태입니다.
     *
     * [특징]
     * - 가입 신청 시 기본값
     * - 모임 멤버 목록에는 표시되지 않음 (승인 대기 목록에만 표시)
     * - 모임 기능(채팅, 정모 참석 등) 이용 불가
     */
    PENDING,

    /**
     * 승인됨
     *
     * 모임장/운영진이 가입을 승인한 상태입니다.
     *
     * [특징]
     * - 정식 멤버로 활동 가능
     * - 모임 멤버 목록에 표시됨
     * - 채팅, 정모 참석 등 모든 기능 이용 가능
     */
    APPROVED,

    /**
     * 거절됨
     *
     * 모임장/운영진이 가입을 거절한 상태입니다.
     *
     * [특징]
     * - 모임 기능 이용 불가
     * - 프론트엔드에서 "가입 거절" 메시지 표시
     * - 재신청 가능 여부는 정책에 따라 결정 (Phase 2에서 구현 예정)
     */
    REJECTED
}
