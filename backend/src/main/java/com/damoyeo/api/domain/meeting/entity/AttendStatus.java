package com.damoyeo.api.domain.meeting.entity;

/**
 * 정모 참석 상태 Enum
 *
 * 회원의 정모 참석 의사를 나타냅니다.
 *
 * - MeetingAttendee 엔티티의 status 필드
 * - MeetingService에서 참석 상태 변경 시
 *
 * [DB 저장]
 * @Enumerated(EnumType.STRING)으로 문자열 저장
 *
 * [프론트엔드 UI 예시]
 * ┌─────────────────────────────────────┐
 * │  정모: 5월 첫째 주 러닝               │
 * │                                      │
 * │  [참석] [미정] [불참]                 │
 * └─────────────────────────────────────┘
 */
public enum AttendStatus {

    /**
     * 참석 예정
     *
     * 정모에 참석하겠다는 의사 표시입니다.
     *
     * [특징]
     * - 참석 신청 시 기본값
     * - 참석 인원 카운트에 포함됨
     * - 정모 정원 확인 시 ATTENDING인 사람 수를 카운트
     *
     * [프론트엔드 표시]
     * "참석 예정 15명" 형태로 표시
     */
    ATTENDING,

    /**
     * 미정
     *
     * 참석 여부를 아직 결정하지 못한 상태입니다.
     *
     * [특징]
     * - 참석 인원 카운트에 포함되지 않음 (또는 별도 표시)
     * - 나중에 ATTENDING이나 NOT_ATTENDING으로 변경 예상
     *
     * [프론트엔드 표시]
     * "미정 3명" 형태로 별도 표시
     */
    MAYBE,

    /**
     * 불참
     *
     * 정모에 참석하지 않겠다는 의사 표시입니다.
     *
     * [특징]
     * - 참석 인원 카운트에 포함되지 않음
     * - 참석자 목록에서 별도 표시 또는 제외
     *
     * [사용 예]
     * 처음에 ATTENDING으로 등록했다가,
     * 일정이 안 맞아서 NOT_ATTENDING으로 변경하는 경우
     */
    NOT_ATTENDING
}
