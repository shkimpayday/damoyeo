package com.damoyeo.api.domain.meeting.entity;

/**
 * ============================================================================
 * 정모 상태 Enum
 * ============================================================================
 *
 * [역할]
 * 정모의 현재 진행 상태를 나타냅니다.
 *
 * [상태 흐름]
 *
 *     정모 생성
 *        │
 *        ▼
 *   ┌──────────┐
 *   │SCHEDULED │  ← 예정됨 (기본 상태)
 *   └────┬─────┘
 *        │
 *        │ 정모 시작 시간 도달
 *        ▼
 *   ┌──────────┐
 *   │ ONGOING  │  ← 진행 중
 *   └────┬─────┘
 *        │
 *        │ 정모 종료
 *        ▼
 *   ┌──────────┐
 *   │COMPLETED │  ← 완료됨
 *   └──────────┘
 *
 *   ※ CANCELLED는 어느 상태에서든 가능
 *
 * [사용 위치]
 * - Meeting 엔티티의 status 필드
 * - MeetingService에서 상태 변경 시
 *
 * [DB 저장]
 * @Enumerated(EnumType.STRING)으로 문자열 저장
 */
public enum MeetingStatus {

    /**
     * 예정됨
     *
     * 정모가 생성되었지만 아직 시작 전인 상태입니다.
     *
     * [특징]
     * - 정모 생성 시 기본값
     * - 참석 신청 가능
     * - 정모 정보 수정 가능
     * - 정모 취소 가능
     */
    SCHEDULED,

    /**
     * 진행 중
     *
     * 정모가 현재 진행 중인 상태입니다.
     *
     * [특징]
     * - 정모 시작 시간이 지나면 자동 또는 수동으로 전환
     * - 추가 참석 신청 불가 (또는 정책에 따라 가능)
     *
     * ※ 자동 상태 변경은 스케줄러로 구현 (Phase 2)
     */
    ONGOING,

    /**
     * 완료됨
     *
     * 정모가 정상적으로 종료된 상태입니다.
     *
     * [특징]
     * - 정모 종료 후 전환
     * - 참석 신청/취소 불가
     * - 참석 기록은 보존됨
     */
    COMPLETED,

    /**
     * 취소됨
     *
     * 정모가 취소된 상태입니다.
     *
     * [특징]
     * - 모임장/운영진/생성자가 취소 가능
     * - 참석 신청/취소 불가
     * - 취소 사유 알림 발송 (Phase 2에서 구현)
     *
     * ※ SCHEDULED 또는 ONGOING 상태에서 전환 가능
     */
    CANCELLED
}
