package com.damoyeo.api.domain.notification.entity;

/**
 * ============================================================================
 * 알림 유형 열거형 (Enum)
 * ============================================================================
 *
 * [역할]
 * 시스템에서 발생하는 모든 알림의 유형을 정의합니다.
 * 알림 유형에 따라 프론트엔드에서 다른 아이콘, 색상, 동작을 적용할 수 있습니다.
 *
 * [사용 위치]
 * - Notification 엔티티의 type 필드
 * - NotificationService.send() 호출 시 파라미터
 * - 프론트엔드에서 알림 아이콘/스타일 분기 처리
 *
 * [DB 저장]
 * @Enumerated(EnumType.STRING)으로 문자열로 저장
 * 예: "NEW_MEMBER", "NEW_MEETING"
 *
 * [알림 유형 분류]
 *
 * ┌─────────────────────────────────────────────────────────────────────────┐
 * │ 유형              │ 발생 시점              │ 수신자            │ 아이콘  │
 * ├─────────────────────────────────────────────────────────────────────────┤
 * │ 정모 관련                                                               │
 * ├─────────────────────────────────────────────────────────────────────────┤
 * │ NEW_MEETING      │ 새 정모가 등록되었을 때 │ 모임 멤버 전체     │ 📅     │
 * │ MEETING_REMINDER │ 정모 시작 전 리마인더   │ 참석 예정자        │ ⏰     │
 * │ MEETING_CANCELLED│ 정모가 취소되었을 때    │ 참석 예정자        │ 🚫     │
 * ├─────────────────────────────────────────────────────────────────────────┤
 * │ 멤버/모임 관련                                                          │
 * ├─────────────────────────────────────────────────────────────────────────┤
 * │ NEW_MEMBER       │ 새 멤버가 가입했을 때   │ 모임장            │ 👋     │
 * │ MEMBER_LEFT      │ 멤버가 탈퇴했을 때      │ 모임장            │ 👋     │
 * │ GROUP_UPDATE     │ 모임 정보가 변경되었을 때│ 모임 멤버 전체    │ 📝     │
 * │ ROLE_CHANGED     │ 역할이 변경되었을 때    │ 해당 멤버         │ 🎖️     │
 * └─────────────────────────────────────────────────────────────────────────┘
 *
 * [프론트엔드 활용 예시]
 * switch (notification.type) {
 *   case 'NEW_MEMBER':
 *     return { icon: '👋', color: 'green', link: `/groups/${relatedId}` };
 *   case 'NEW_MEETING':
 *     return { icon: '📅', color: 'blue', link: `/meetings/${relatedId}` };
 *   ...
 * }
 *
 * [확장 가능성]
 * 새로운 알림 유형이 필요하면 여기에 추가합니다.
 * 예: CHAT_MENTION (채팅에서 멘션), COMMENT_REPLY (댓글 답글) 등
 */
public enum NotificationType {

    // ========================================================================
    // 정모 관련 알림
    // ========================================================================

    /**
     * 새 정모 등록
     *
     * [발생 시점]
     * 모임에 새로운 정모가 등록되었을 때
     *
     * [수신자]
     * 해당 모임의 모든 멤버
     *
     * [relatedId]
     * meetingId (해당 정모 ID)
     *
     * [예시 메시지]
     * "[강남 러닝 크루] 새 정모가 등록되었습니다: 5월 첫째 주 러닝"
     *
     * [프론트엔드 동작]
     * 알림 클릭 시 정모 상세 페이지로 이동
     */
    NEW_MEETING,

    /**
     * 정모 리마인더
     *
     * [발생 시점]
     * 정모 시작 24시간 전, 1시간 전 등 (스케줄러로 발송)
     *
     * [수신자]
     * 참석 예정자 (ATTENDING 상태인 멤버)
     *
     * [relatedId]
     * meetingId (해당 정모 ID)
     *
     * [예시 메시지]
     * "[5월 첫째 주 러닝] 정모가 내일 오전 10시에 시작됩니다!"
     *
     * [구현 참고]
     * Spring @Scheduled 또는 Quartz를 사용하여 구현
     */
    MEETING_REMINDER,

    /**
     * 정모 취소
     *
     * [발생 시점]
     * 정모가 취소되었을 때
     *
     * [수신자]
     * 참석/미정으로 등록한 멤버 (ATTENDING, MAYBE 상태)
     *
     * [relatedId]
     * meetingId (해당 정모 ID)
     *
     * [예시 메시지]
     * "[5월 첫째 주 러닝] 정모가 취소되었습니다."
     */
    MEETING_CANCELLED,

    /**
     * 정모 정보 변경
     *
     * [발생 시점]
     * 정모의 장소/시간 등이 변경되었을 때
     *
     * [수신자]
     * 참석 예정자 (ATTENDING 상태인 멤버)
     *
     * [relatedId]
     * meetingId (해당 정모 ID)
     *
     * [예시 메시지]
     * "'{정모명}' 일정이 변경되었습니다. 확인해주세요."
     */
    MEETING_UPDATED,

    /**
     * 정모 임박 알림 (3시간 전)
     *
     * [발생 시점]
     * 정모 시작 3시간 전 (스케줄러로 발송)
     *
     * [수신자]
     * 참석 예정자 (ATTENDING 상태인 멤버)
     *
     * [relatedId]
     * meetingId (해당 정모 ID)
     *
     * [예시 메시지]
     * "'{정모명}'이 3시간 후 시작됩니다."
     */
    MEETING_IMMINENT,

    // ========================================================================
    // 멤버/모임 관련 알림
    // ========================================================================

    /**
     * 새 멤버 가입
     *
     * [발생 시점]
     * 새 멤버가 모임에 가입했을 때
     *
     * [수신자]
     * 모임장
     *
     * [relatedId]
     * groupId (해당 모임 ID)
     *
     * [예시 메시지]
     * "김철수님이 [강남 러닝 크루]에 가입했습니다."
     */
    NEW_MEMBER,

    /**
     * 멤버 탈퇴
     *
     * [발생 시점]
     * 멤버가 모임을 탈퇴했을 때
     *
     * [수신자]
     * 모임장
     *
     * [relatedId]
     * groupId (해당 모임 ID)
     *
     * [예시 메시지]
     * "김철수님이 [강남 러닝 크루]에서 탈퇴했습니다."
     */
    MEMBER_LEFT,

    /**
     * 모임 정보 변경
     *
     * [발생 시점]
     * 모임 정보가 수정되었을 때 (이름, 설명, 위치 등)
     *
     * [수신자]
     * 해당 모임의 모든 멤버
     *
     * [relatedId]
     * groupId (해당 모임 ID)
     *
     * [예시 메시지]
     * "[강남 러닝 크루] 모임 정보가 업데이트되었습니다."
     *
     * [구현 참고]
     * 중요하지 않은 변경은 알림을 보내지 않을 수 있음
     * (예: 커버 이미지만 변경 시)
     */
    GROUP_UPDATE,

    /**
     * 역할 변경
     *
     * [발생 시점]
     * 멤버의 역할이 변경되었을 때
     * (MEMBER → MANAGER, MANAGER → MEMBER 등)
     *
     * [수신자]
     * 역할이 변경된 멤버 본인
     *
     * [relatedId]
     * groupId (해당 모임 ID)
     *
     * [예시 메시지]
     * "[강남 러닝 크루]에서 운영진으로 임명되었습니다."
     * "[강남 러닝 크루]에서 운영진 권한이 해제되었습니다."
     */
    ROLE_CHANGED,

    /**
     * 멤버 강퇴
     *
     * [발생 시점]
     * 모임장 또는 운영진이 멤버를 강퇴했을 때
     *
     * [수신자]
     * 강퇴된 멤버
     *
     * [relatedId]
     * groupId (해당 모임 ID)
     *
     * [예시 메시지]
     * "'강남 러닝 크루'에서 탈퇴 처리되었습니다."
     */
    MEMBER_KICKED,

    /**
     * 모임 해체
     *
     * [발생 시점]
     * 모임장이 모임을 삭제(해체)했을 때
     *
     * [수신자]
     * 모임의 모든 멤버 (모임장 제외)
     *
     * [relatedId]
     * groupId (해당 모임 ID)
     *
     * [예시 메시지]
     * "'강남 러닝 크루'이 해체되었습니다."
     */
    GROUP_DISBANDED,

    // ========================================================================
    // 시스템 알림
    // ========================================================================

    /**
     * 회원가입 환영
     *
     * [발생 시점]
     * 회원가입이 완료되었을 때
     *
     * [수신자]
     * 신규 가입 회원
     *
     * [relatedId]
     * null (관련 리소스 없음)
     *
     * [예시 메시지]
     * "다모여에 오신 것을 환영합니다! 🎉
     *  첫 가입 기념으로 프리미엄 30일 무료 쿠폰을 드립니다."
     *
     * [프론트엔드 동작]
     * 알림 클릭 시 쿠폰함 또는 프로필 페이지로 이동
     */
    WELCOME
}
